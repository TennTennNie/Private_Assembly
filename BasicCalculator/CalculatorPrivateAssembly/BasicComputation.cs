using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CalculatorPrivateAssembly
{
    public class BasicComputation
    {
        public float Addition(int x, int y)
        {
            return x + y;
        }

        public float Subtraction(int x, int y)
        {
            return x - y;
        }

        public float Multiplication(int x, int y)
        {
            return x * y;
        }

        public float Divition(int x, int y)
        {
            return x / y;
        }
    }
}
